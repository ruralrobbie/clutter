// Function to switch language
function switchLanguage(lang) {
    const elements = document.querySelectorAll('[data-lang-' + lang + ']');
    elements.forEach(el => {
        el.innerHTML = el.getAttribute('data-lang-' + lang);
    });
    document.documentElement.lang = lang;
}

// Event listeners
